"""
escape sequnce is used in double quote or single quote
\n=>to new line
\t=>to give tab
\\=>to get a single slash in terminal
''=>used in print if we want double quote in print
\"=>used in print if we want double quote in print and print helper
\'=>used in print if we want single quote in print and print helper
"""

print("My name is tirth\nMy age is 18\nMy gender is male")
print("My name is tirth\tMy age is 18\tMy gender is male")
print("tirth\\parikh")
print('tirth"s box')
print("tirth's box")
print('My name is \\"Tirth\\"\\')
